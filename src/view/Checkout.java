    private void btnCheckoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckoutActionPerformed
        // TODO add your handling code here:
        ExecuteTransaksi ex  = new ExecuteTransaksi();
        int total =  Integer.parseInt(txtTotal.getText());
        int uang_pasien = Integer.parseInt(txtUangPasien.getText());
        ex.ubahDataTotal(id_transaksi, total);
        if( uang_pasien < total){
            JOptionPane.showMessageDialog(btnCheckout, "Uang anda kurang!");
        }else{
            int kembalian = uang_pasien - total;
            JOptionPane.showMessageDialog(btnCheckout, "Transaksi berhasil! \n"
                    + "Kembalian : " + kembalian);
            taStruk.append("==================================" + "\n");
            taStruk.append("Total : " + total + "\n");
            taStruk.append("Kembalian : " + kembalian);
        }
    }//GEN-LAST:event_btnCheckoutActionPerformed

