    private void setDataStruk(){
        ExecuteTransaksiItem ex = new ExecuteTransaksiItem();
        List<TransaksiItem> lsTransaksi = ex.getAllDataByTransaksi(id_transaksi);
        PreparedStatement ps;
        PreparedStatement ps2;
        try{
            ConnectionManager conMan = new ConnectionManager();
            Connection conn = conMan.logOn();
            ps = conn.prepareStatement("select * from transaksi where id_transaksi=?");
            ps2 = conn.prepareStatement("select SUM(subtotal) as total from transaksi_item where id_transaksi=?");
            ps.setInt(1, id_transaksi);
            ps2.setInt(1, this.id_transaksi);
            ResultSet rs = ps.executeQuery();
            ResultSet rs2 = ps2.executeQuery();
            if(rs.next()){
                Transaksi tr = new Transaksi();
                tr.setInvoice(rs.getString("invoice"));
                taStruk.append("Invoice : " + tr.getInvoice() + "\n");
                System.out.println(tr.getInvoice());
                if (rs2.next()) {                    
                    int total = rs2.getInt("total");
                    
                    txtTotal.setText(String.valueOf(total));
                }
            }
        }catch(SQLException exception){
            exception.printStackTrace();
        }
        taStruk.append("==================================" + "\n");
        for(TransaksiItem t : lsTransaksi){
            taStruk.append(t.getObat().getNama() + "              " + "Rp." +t.getObat().getHarga() + "\n");
        }
        
    }
