    private void btnSubmitItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitItemActionPerformed
        // TODO add your handling code here:
        String qty = txtQtyTransaksiItem.getText();
        String subtotal = txtSubtotalTransaksiItem.getText();
        int id_obat = ((Obat)cbbObat.getSelectedItem()).getId_obat();
        TransaksiItem t = new TransaksiItem(this.id_transaksi, id_obat, Integer.parseInt(qty), Integer.parseInt(subtotal));
        ExecuteTransaksiItem ex = new ExecuteTransaksiItem();
        int hasil = ex.insertData(t);
        if(hasil >0){
            JOptionPane.showMessageDialog(null, "Data berhasil di simpan");
            setDataTransaksiItem(this.id_transaksi);
            setTotal();
        }
        else{
            JOptionPane.showMessageDialog(null, "Data gagal di simpan");
        }
    }//GEN-LAST:event_btnSubmitItemActionPerformed

    private void tblTransaksiItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransaksiItemMouseClicked
        // TODO add your handling code here:
        int row = tblTransaksiItem.getSelectedRow();
        String id = tblTransaksiItem.getValueAt(row, 0).toString();
        String id_transaksi = tblTransaksiItem.getValueAt(row, 1).toString();
        String id_obat = tblTransaksiItem.getValueAt(row, 2).toString();
        String qty = tblTransaksiItem.getValueAt(row, 3).toString();
        String subtotal = tblTransaksiItem.getValueAt(row, 4).toString();
        this.id_transaksi_item = Integer.parseInt(id);
        txtQtyTransaksiItem.setText(qty);
        txtSubtotalTransaksiItem.setText(subtotal);
    }//GEN-LAST:event_tblTransaksiItemMouseClicked

    private void btnUpdateItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateItemActionPerformed
        // TODO add your handling code here:
        String qty = txtQtyTransaksiItem.getText();
        String subtotal = txtSubtotalTransaksiItem.getText();
        int id_obat = ((Obat)cbbObat.getSelectedItem()).getId_obat();
        TransaksiItem t = new TransaksiItem(this.id_transaksi_item, this.id_transaksi, id_obat, Integer.parseInt(qty), Integer.parseInt(subtotal));
        ExecuteTransaksiItem ex = new ExecuteTransaksiItem();
        int hasil = ex.ubahData(t);
        if(hasil >0){
            JOptionPane.showMessageDialog(null, "Data berhasil di ubah");
            setDataTransaksiItem(this.id_transaksi);
            setTotal();
        }
        else{
            JOptionPane.showMessageDialog(null, "Data gagal di ubah");
        }
    }//GEN-LAST:event_btnUpdateItemActionPerformed

