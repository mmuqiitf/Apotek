    private void setDataTransaksiItem(int id_transaksi){
        ConvertTransaksiItemToObject cpto = new ConvertTransaksiItemToObject();
        String[][] dataTransaksiItem = cpto.getTransaksi(id_transaksi);
        tblTransaksiItem.setModel(new javax.swing.table.DefaultTableModel(
            dataTransaksiItem,
            new String [] {
                "ID Transaksi Item", "ID Transaksi", "ID Obat", "Qty", "Subtotal", "Obat", "Invoice"
            }
        ));
        jScrollPane1.setViewportView(tblTransaksiItem);
        PreparedStatement ps;
        try{
            ConnectionManager conMan = new ConnectionManager();
            Connection conn = conMan.logOn();
            ps = conn.prepareStatement("select * from obat");
            Vector<Obat> vector = new Vector<>();
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                  vector.addElement(new Obat(rs.getInt(1), rs.getString(2), rs.getInt(3)));
                  cbbObat.setModel(new DefaultComboBoxModel(vector));
            }
        }catch(SQLException sQLException){
            sQLException.printStackTrace();
        }
    }
    
    private void setTotal(){
        PreparedStatement ps;
        PreparedStatement ps2;
        try{
            ConnectionManager conMan = new ConnectionManager();
            Connection conn = conMan.logOn();
            ps = conn.prepareStatement("select SUM(subtotal) AS total from transaksi_item where id_transaksi=?");
            ps.setInt(1, this.id_transaksi);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                int total = rs.getInt("total");
                ps2 = conn.prepareStatement("update transaksi set total=? where id_transaksi=?");
                ps2.setInt(1, total);
                ps2.setInt(2, this.id_transaksi);
                int hasil = ps2.executeUpdate();
                if(hasil > 0){
                    JOptionPane.showMessageDialog(null, "Total harga telah diubah menjadi " + total);
                }
            }
        }catch(SQLException exception){
            exception.printStackTrace();
        }
    }
